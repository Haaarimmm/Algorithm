#include <iostream>
#include <string>

#define endl "\n"

using namespace std;

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    string board[5];
    char num[10][5][3] = {{{'*', '*', '*'},
                           {'*', ' ', '*'},
                           {'*', ' ', '*'},
                           {'*', ' ', '*'},
                           {'*', '*', '*'}},
                          {{' ', ' ', '*'},
                           {' ', ' ', '*'},
                           {' ', ' ', '*'},
                           {' ', ' ', '*'},
                           {' ', ' ', '*'}},
                          {{'*', '*', '*'},
                           {' ', ' ', '*'},
                           {'*', '*', '*'},
                           {'*', ' ', ' '},
                           {'*', '*', '*'}},
                          {{'*', '*', '*'},
                           {' ', ' ', '*'},
                           {'*', '*', '*'},
                           {' ', ' ', '*'},
                           {'*', '*', '*'}},
                          {{'*', ' ', '*'},
                           {'*', ' ', '*'},
                           {'*', '*', '*'},
                           {' ', ' ', '*'},
                           {' ', ' ', '*'}},
                          {{'*', '*', '*'},
                           {'*', ' ', ' '},
                           {'*', '*', '*'},
                           {' ', ' ', '*'},
                           {'*', '*', '*'}},
                          {{'*', '*', '*'},
                           {'*', ' ', ' '},
                           {'*', '*', '*'},
                           {'*', ' ', '*'},
                           {'*', '*', '*'}},
                          {{'*', '*', '*'},
                           {' ', ' ', '*'},
                           {' ', ' ', '*'},
                           {' ', ' ', '*'},
                           {' ', ' ', '*'}},
                          {{'*', '*', '*'},
                           {'*', ' ', '*'},
                           {'*', '*', '*'},
                           {'*', ' ', '*'},
                           {'*', '*', '*'}},
                          {{'*', '*', '*'},
                           {'*', ' ', '*'},
                           {'*', '*', '*'},
                           {' ', ' ', '*'},
                           {'*', '*', '*'}}};

    for (int i = 0; i < 5; i++)
        getline(cin, board[i]);

    int idx = 0;
    int ans = 0;
    int end = board[0].length();
    bool flag;

    while (idx < end)
    {
        for (int k = 0; k < 10; k++)
        {
            flag = true;
            for (int i = 0; i < 5; i++)
            {
                for (int j = idx; j < idx + 3; j++)
                {
                    if (board[i][j] != num[k][i][j - idx])
                    {
                        flag = false;
                        break;
                    }
                }
                if (!flag)
                    break;
            }
            if (flag)
            {
                ans = ans * 10 + k;
                idx += 4;
                break;
            }
        }
        if (!flag)
        {
            cout << "BOOM!!" << endl;
            return 0;
        }
    }

    if (ans % 6 == 0)
        cout << "BEER!!" << endl;
    else
        cout << "BOOM!!" << endl;

    return 0;
}